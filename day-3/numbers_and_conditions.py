# Example 2: If-else condition
marks = 72
if marks >= 33:
    print("Pass")
else:
    print("Fail")
# Output: Pass


# Example 3: Using input with if-else
number = int(input("Enter a number: "))
if number % 2 == 0:
    print("Even number")
else:
    print("Odd number")
# Example Input: 7
# Output: Odd number


# Example 4: Find maximum of three numbers
