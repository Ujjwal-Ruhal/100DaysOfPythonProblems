# Program to convert Celsius temperature to Fahrenheit

# Taking input from the user (temperature in Celsius)
celsius = float(input("Enter the value in Celsius: "))

# Formula to convert Celsius to Fahrenheit
# Fahrenheit = (Celsius Ã— 9/5) + 32
fahrenheit = (celsius * 9/5) + 32

# Displaying the result
print("Fahrenheit is:", fahrenheit)
